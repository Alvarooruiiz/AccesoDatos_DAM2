package Actividad_4_4_3;

public class EncabezadoBMP {
    int tamano;
    int ancho;
    int alto;
    boolean comprimida;

    public EncabezadoBMP(int tamano, int ancho, int alto, boolean comprimida) {
        this.tamano = tamano;
        this.ancho = ancho;
        this.alto = alto;
        this.comprimida = comprimida;
    }
}